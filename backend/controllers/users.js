module.exports.login = (req, res, next) => {
  const { email, password } = req.body;

  return User.findUserByCredentials(email, password)
    .then((user) => {
      const secret = req.app.get('secret');
      // создадим токен
      const token = jwt.sign({ _id: user._id }, secret, { expiresIn: '7d' });

      // вернём токен
      res.send({ token });
    })
    .catch((err) => {
      next(err);
    });
};
